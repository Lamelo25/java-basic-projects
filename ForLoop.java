public class ForLoop{
	public static void main(String[] args) {
		

		for (int x=97;x>=91 ;x-- ) {
			System.out.println(x);

		}
		for(int x=108; x>=105; x--){
			System.out.print(x+"\n");
		}
		System.out.println("\n");
		for (int x=1;x<=2 ;x++ ) {
			for (int y=1;y<=5 ;y++ ) {
				System.out.print("*\t");
				
			}
			System.out.println("\n");
				
		}
		
		
	}
}